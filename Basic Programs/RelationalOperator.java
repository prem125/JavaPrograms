class RelationalOperator 
{
	public static void main(String[] args) 
	{
		int i=30,j=30;
		boolean res1= i==j;
		boolean res2= i!=j;
		boolean res3= i>j;
		boolean res4= i<j;
		boolean res5= i>=j;
		boolean res6= i<=j;
		System.out.println(res1);
		System.out.println(res2);
		System.out.println(res3);
		System.out.println(res4);
		System.out.println(res5);
		System.out.println(res6);
	}
}
